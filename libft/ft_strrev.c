#include "libft.h"

void	ft_strrev(char *s)
{
	char *e;
	char t;

	if (!s)
		return ;
	e = s;
	while (*e)
		e++;
	e--;
	while (s < e)
	{
		t = *s;
		*s++ = *e;
		*e-- = t;
	}
}
