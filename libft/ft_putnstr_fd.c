#include "libft.h"

int	ft_putnstr_fd(int fd, char *s, int n)
{
	int count;

	count = 0;
	if (n > 0)
	{
		while (*s && n-- > 0)
		{
			ft_putchar_fd(*s++, fd);
			count++;
		}
	}
	return (count);
}
