#include "libft.h"

size_t	ft_wstrlen(const char *s)
{
	size_t n;
	unsigned char c;

	n = 0;
	if (s)
		while ((c = (unsigned char)*s++))
			if (c >> 7 == 0 || c >> 5 == 0x6 || c >> 4 == 0xE || c >> 3 == 0x1E)
				n++;
	return (n);
}
