int		ft_wordcount(char *s, char delim)
{
	int i;

	i = 0;
	if (s && *s)
	{
		if (*s != delim)
			i++;
		while (*(s + 1))
		{
			if (*s == delim && *(s + 1) != delim)
				i++;
			s++;
		}
	}
	return (i);
}
