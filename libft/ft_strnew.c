#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*s;
	size_t	i;

	s = (char*)malloc(sizeof(*s) * size + 1);
	if (s)
	{
		i = 0;
		while (i <= size)
			s[i++] = 0;
	}
	return (s);
}
